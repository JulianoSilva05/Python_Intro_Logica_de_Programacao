#Tipos de dados
"""
Python = Linguagem de programação
Tipos de tipagem = Dinâmica / Forte # Sabe qual o tipo de dado que estamos passando 
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)#Dinamica, Python já sabe qual o tipo de dado
# Aspas simples
print('Juliano', end=" ")
# Aspas duplas
print("Silva")
print(type("Silva"))
print(type(True))

# Escape
print('Juliano \" Silva') #Escape é usado para não interpretar um caractere que tem função, exemplo: " é usado para iniciar um texto
# r se quiser que o caractere de escape apareça
print(r"Juliano \" Silva")
#outra solução
print('Juliano " Silva')

